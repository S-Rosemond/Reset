// O(n) time

var findMaxConsecutiveOnes = function (nums) {
  let max = 0;
  let current = 0;

  for (el of nums) {
    if (el === 1) {
      current++;
    } else {
      max = Math.max(current, max);
      current = 0;
    }
  }

  return current > max ? current : max;
};

